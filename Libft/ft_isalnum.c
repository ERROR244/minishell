#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c < 'Z' + 1) || (c >= 'a' && c <= 'z'))
		return (1);
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
